const treesIWillHit = (map, downSlope, rightSlope) => {
  let treesHit = 0;
  let mapArray = forestMap.split('\n');

  const travelRecursion = (downwardPosition = 0, rightwardPosition = 0) => {
    if (mapArray[downwardPosition] !== undefined) {
      if (mapArray[downwardPosition][rightwardPosition] === '#') {
        treesHit++;
      }
      rightwardPosition += rightSlope;

      if (rightwardPosition > mapArray[downwardPosition].length - 1) {
        rightwardPosition -= mapArray[downwardPosition].length;
      }
      downwardPosition += downSlope;
      travelRecursion(downwardPosition, rightwardPosition)
    }
  }
  travelRecursion();
  return treesHit;
}

const path1 = treesIWillHit(forestMap, 1, 1);
const path2 = treesIWillHit(forestMap, 1, 3);
const path3 = treesIWillHit(forestMap, 1, 5);
const path4 = treesIWillHit(forestMap, 1, 7);
const path5 = treesIWillHit(forestMap, 2, 1);

console.log(path1 * path2 * path3 * path4 * path5);